package com.app.master;

public class Department{
	
	private Integer id;
	private String name;
	
	// --------- GETTER AND SETTER -----------
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}

}
